"""

Завдання 4. Розробити програму для розрахунку дальності польоту снаряда за
заданими початковою швидкістю та кутом вильоту. Вважати умови ідеальними.
Програма має обчислювати та виводити максимальну висоту, а також висоту снаряда
на кожну секунду польоту.

"""

import math

try:
    v0 = float(input("Введіть початкову швидкість (м/с): "))
    angle = float(input("Введіть кут вильоту (градуси): "))

    g = 9.81
    angle_rad = math.radians(angle)

    t_flight = (2 * v0 * math.sin(angle_rad)) / g

    h_max = (v0 ** 2 * (math.sin(angle_rad) ** 2)) / (2 * g)

    print(f"\nМаксимальна висота підйому: {h_max:.2f} м")
    print(f"Загальний час польоту: {t_flight:.2f} с")
    print("-" * 30)
    print("Висота снаряда щосекунди:")

    t = 0
    while t <= t_flight:
        y = v0 * math.sin(angle_rad) * t - 0.5 * g * t ** 2
        print(f"Час: {t} с -> Висота: {y:.2f} м")
        t += 1

    if t > t_flight:
        print(f"Час: {t_flight:.2f} с -> Висота: 0.00 м (Приземлення)")

except ValueError:
    print("Помилка: введіть коректні числові дані.")
