"""

Завдання 7. Сформувати список об’єктів Dog, Cat, Cow і в циклі викликати sound()
для кожного елемента, супроводивши виконання коротким поясненням того, як у
Python працює пізнє зв’язування (динамічна диспетчеризація методів під час
виконання).

"""

class Animal:
    def sound(self):
        print("Невідомий звук")

class Dog(Animal):
    def sound(self):
        print("Гав-гав!")

class Cat(Animal):
    def sound(self):
        print("Мяу!")

class Cow(Animal):
    def sound(self):
        print("Му-у-у!")

zoo = [Dog(), Cat(), Cow()]

print("--- Демонстрація поліморфізму ---")

for animal in zoo:
    animal.sound()

print("\n--- Пояснення пізнього зв'язування ---")
print("Під час написання коду (for animal in zoo) інтерпретатор ще не знає,")
print("який саме метод sound() буде викликано (чи це собака, чи кіт).")
print("Рішення про те, який код виконати, приймається безпосередньо в момент")
print("виконання програми (runtime), залежно від типу об'єкта, що потрапив у змінну.")
