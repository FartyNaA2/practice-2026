import pandas as pd

data = {
    'Прізвище': ['Петренко', 'Коваль', 'Сидоренко', 'Бойко', 'Гнатюк'],
    'Ім\'я': ['Іван', 'Марія', 'Олег', 'Анна', 'Дмитро'],
    'Математика': [90, 100, 75, 88, 60],
    'Фізика': [85, 98, 80, 90, 65],
    'Історія': [92, 95, 78, 85, 70],
    'Інформатика': [88, 99, 82, 92, 68],
    'Англійська': [95, 100, 70, 89, 72]
}

df_initial = pd.DataFrame(data)
df_initial.to_csv('group_grades.csv', index=False, encoding='utf-8')

df = pd.read_csv('group_grades.csv')

subjects = ['Математика', 'Фізика', 'Історія', 'Інформатика', 'Англійська']

df['Середній бал'] = df[subjects].mean(axis=1).round(2)

print("--- Зведена таблиця успішності ---")
print(df.to_string(index=False))

print("\n--- Середній бал групи по предметах ---")
group_stats = df[subjects].mean(axis=0).round(2)
print(group_stats)
