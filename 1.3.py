"""

Завдання 3. Створити програму для розв’язування квадратного рівняння. Коефіцієнти
рівняння вводяться користувачем з клавіатури.

"""
import math

print("Розв'язування квадратного рівняння: ax^2 + bx + c = 0")

try:
    a = float(input("Введіть коефіцієнт a: "))
    b = float(input("Введіть коефіцієнт b: "))
    c = float(input("Введіть коефіцієнт c: "))

    if a == 0:
        print("Це не квадратне рівняння, оскільки a = 0.")
    else:
        D = b**2 - 4*a*c
        print(f"Дискримінант D = {D}")

        if D > 0:
            x1 = (-b + math.sqrt(D)) / (2*a)
            x2 = (-b - math.sqrt(D)) / (2*a)
            print(f"Рівняння має два корені:\nx1 = {x1}\nx2 = {x2}")
        elif D == 0:
            x = -b / (2*a)
            print(f"Рівняння має один корінь:\nx = {x}")
        else:
            print("Дискримінант менше нуля. Дійсних коренів немає.")

except ValueError:
    print("Помилка: введіть коректні числові значення.")
